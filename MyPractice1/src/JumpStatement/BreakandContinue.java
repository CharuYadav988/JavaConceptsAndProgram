package JumpStatement;

public class BreakandContinue {

	public static void main(String[] args) {
		int n=10;
		 for(int i=0;i<=n;i++)
		 {
			 if(i==5)
			 {
				 
				 break;
			 }
			 System.out.println(i);
		 }
		 
		 int n1=10;
		 for(int j=0;j<=n1;j++)
		 {
			 if(j==5)
			 {
				 
				 continue;
			 }
			 System.out.println(j);
		 }

	}

}
